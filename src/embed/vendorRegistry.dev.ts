/**
 * Lookup including the dynamic test vendor generated by the SDK test site.
 * Called by findVendor() — returns the dynamic entry if kid matches, else
 * falls through to the static TEST_VENDOR_REGISTRY.
 */
export function findTestVendor(kid: string): VendorRegistryEntry | undefined {
  const dynamic = getDynamicVendorEntry()
  if (dynamic && dynamic.kid === kid) return dynamic
  return TEST_VENDOR_REGISTRY.find((v) => v.kid === kid)
}
